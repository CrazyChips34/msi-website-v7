  // Helper function to get unique grades
  export const getUniqueGrades = (): string[] => {
    return [...new Set(examPapers.map(paper => paper.grade))];
  };
  
  // Helper function to get unique subjects
  export const getUniqueSubjects = (): string[] => {
    return [...new Set(examPapers.map(paper => paper.subject))];
  };
  
  // Helper function to get unique years
  export const getUniqueYears = (): string[] => {
    return [...new Set(examPapers.map(paper => paper.year))].sort((a, b) => parseInt(b) - parseInt(a));
  };
  
  // Helper function to get unique exam types
  export const getUniqueExamTypes = (): string[] => {
    return [...new Set(examPapers.map(paper => paper.examType))];
  };
  
  // Helper function to filter papers by criteria
  export const filterPapers = (
    gradeFilter?: string,
    subjectFilter?: string,
    yearFilter?: string,
    examTypeFilter?: string
  ): ExamPaper[] => {
    return examPapers.filter(paper => {
      if (gradeFilter && paper.grade !== gradeFilter) return false;
      if (subjectFilter && paper.subject !== subjectFilter) return false;
      if (yearFilter && paper.year !== yearFilter) return false;
      if (examTypeFilter && paper.examType !== examTypeFilter) return false;
      return true;
    });
  };
  
  // Export default for easier importing
  export default examPapers;